namespace Bhp\Util\GhProxy;
class GhProxy
{
    /**
     * @param string $url
     * @return string
     */
    public static function mirror(string $url): string
    {
        if (!getEnable('network_github') || $url == '') return $url;
        //
        if (($mirror = getConf('network_github.mirror')) != '') {
            return $mirror . $url;
        }
        //
        return $url;
    }
}
